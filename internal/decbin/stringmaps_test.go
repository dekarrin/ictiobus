func Test_EncMapStringToInt(t *testing.T) {
	testCases := []struct {
		name   string
		input  map[string]int
		expect []byte
	}{
		{
			name:   "empty",
			input:  map[string]int{},
			expect: []byte{0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00},
		},
		{
			name:   "nil",
			input:  nil,
			expect: []byte{0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00},
		},
		{
			name: "one item",
			input: map[string]int{
				"entry": 1,
			},
			expect: []byte{
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x15,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x05, 0x65, 0x6e, 0x74, 0x72, 0x79,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01,
			},
		},
		{
			name: "multiple items",
			input: map[string]int{
				"first":  1,
				"second": 35,
				"count":  2,
			},
			expect: []byte{
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x40,

				// is always alphabetical, so count first
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x05, 0x63, 0x6f, 0x75, 0x6e, 0x74,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x02,

				// first
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x05, 0x66, 0x69, 0x72, 0x73, 0x74,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01,

				// second
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x06, 0x73, 0x65, 0x63, 0x6f, 0x6e, 0x64,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x23,
			},
		},
	}

	for _, tc := range testCases {
		t.Run(tc.name, func(t *testing.T) {
			assert := assert.New(t)

			actual := EncMapStringToInt(tc.input)

			assert.Equal(tc.expect, actual)
		})
	}
}

func Test_DecMapStringToInt(t *testing.T) {
	testCases := []struct {
		name        string
		input       []byte
		expectValue map[string]int
		expectRead  int
		expectError bool
	}{
		{
			name:        "empty",
			input:       []byte{0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00},
			expectValue: map[string]int{},
			expectRead:  8,
		},
		{
			name: "one item, and extra bytes",
			input: []byte{

				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x15,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x05, 0x65, 0x6e, 0x74, 0x72, 0x79,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01,

				// extra
				0x023, 0x04, 0x0ff, 0xee,
			},
			expectValue: map[string]int{
				"entry": 1,
			},
			expectRead: 29,
		},
		{
			name: "multiple items",
			input: []byte{
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x40,

				// is always alphabetical, so count first
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x05, 0x63, 0x6f, 0x75, 0x6e, 0x74,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x02,

				// first
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x05, 0x66, 0x69, 0x72, 0x73, 0x74,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01,

				// second
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x06, 0x73, 0x65, 0x63, 0x6f, 0x6e, 0x64,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x23,
			},
			expectValue: map[string]int{
				"first":  1,
				"second": 35,
				"count":  2,
			},
			expectRead: 72,
		},
		{
			name: "not enough bytes for map",
			input: []byte{
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x15,
				0x00, 0x00, 0x00,
			},
			expectError: true,
		},
		{
			name: "not enough bytes for map key",
			input: []byte{
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x0c,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x05, 0x65, 0x6e, 0x74, 0x72,
			},
			expectError: true,
		},
		{
			name: "not enough bytes for map value",
			input: []byte{
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x13,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x05, 0x65, 0x6e, 0x74, 0x72, 0x79,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
			},
			expectError: true,
		},
		{
			name: "missing map value",
			input: []byte{
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x0d,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x05, 0x65, 0x6e, 0x74, 0x72, 0x79,
			},
			expectError: true,
		},
	}

	for _, tc := range testCases {
		t.Run(tc.name, func(t *testing.T) {
			assert := assert.New(t)

			actualValue, actualRead, actualError := DecMapStringToInt(tc.input)
			if tc.expectError {
				assert.Error(actualError)
				return
			} else if !assert.NoError(actualError) {
				return
			}

			assert.Equal(tc.expectValue, actualValue)
			assert.Equal(tc.expectRead, actualRead)
		})
	}
}

func Test_EncMapStringToBinary(t *testing.T) {
	testCases := []struct {
		name   string
		input  map[string]encoding.BinaryMarshaler
		expect []byte
	}{
		{
			name:   "empty",
			input:  map[string]encoding.BinaryMarshaler{},
			expect: []byte{0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00},
		},
		{
			name:   "nil",
			input:  nil,
			expect: []byte{0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00},
		},
		{
			name: "one item",
			input: map[string]encoding.BinaryMarshaler{
				"item": valueThatMarshalsWith(func() []byte {
					return []byte{0x03, 0xaf, 0xff, 0xde, 0x83}
				}),
			},
		},
		{
			name: "multiple items",
			input: map[string]encoding.BinaryMarshaler{
				"someItem": valueThatMarshalsWith(func() []byte {
					return []byte{0x03, 0xaf, 0xff, 0xde, 0x83}
				}),
				"other": valueThatMarshalsWith(func() []byte {
					return []byte{}
				}),
				"Fran√ßais": valueThatMarshalsWith(func() []byte {
					return []byte{0x04, 0x04, 0x04, 0xe6}
				}),
			},
		},
	}

	for _, tc := range testCases {
		t.Run(tc.name, func(t *testing.T) {
			assert := assert.New(t)

			actual := EncMapStringToBinary(tc.input)

			assert.Equal(tc.expect, actual)
		})
	}
}
